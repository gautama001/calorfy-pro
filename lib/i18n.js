export function normalizeLanguage(value) {
  const code = String(value || '').toLowerCase().split('-')[0];
  return LANGUAGES.includes(code) ? code : DEFAULT_LANGUAGE;
}

export function localeFor(language) {
  return { es: 'es-AR', en: 'en-US', pt: 'pt-BR' }[normalizeLanguage(language)];
}

export function translate(language, key, variables = {}) {
  const template = copy[normalizeLanguage(language)]?.[key] ?? copy.es[key] ?? key;
  return Object.entries(variables).reduce(
    (text, [name, value]) => text.replaceAll(`{{${name}}}`, String(value)),
    template,
  );
}
